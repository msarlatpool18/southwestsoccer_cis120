<h2>American Rivers Conference Information</h2>     
<p>Honoring its heritage in Iowa and its expansion outside the state, the American Rivers Conference brand was established on August 9, 2018, to build on the academic and athletic success provided by its legacy, the Iowa Intercollegiate Athletic Conference.</p>
<p>The name centers on key geographic features of the conference's birthplace. The Mississippi to the east, the most famous river in America and one of the most famous in the world, and the Missouri to the west, the country's longest river, make up most of the State of Iowa's borders. The conference's regional and national reach is reflected in the name.</p>
